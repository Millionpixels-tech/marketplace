// Keyword density and optimization functions
export const generateOptimizedKeywords = (primaryKeyword: string, category?: string): string[] => {
  const baseKeywords = [primaryKeyword];
  
  // Add variations
  baseKeywords.push(
    `${primaryKeyword} online`,
    `buy ${primaryKeyword}`,
    `authentic ${primaryKeyword}`,
    `handmade ${primaryKeyword}`,
    `traditional ${primaryKeyword}`,
    `${primaryKeyword} Sri Lanka`,
    `${primaryKeyword} delivery`,
    `${primaryKeyword} shop`
  );
  
  // Add category-specific keywords
  if (category && CATEGORY_KEYWORDS[category]) {
    baseKeywords.push(...CATEGORY_KEYWORDS[category].slice(0, 3));
  }
  
  // Add long-tail variations
  baseKeywords.push(...LONG_TAIL_KEYWORDS.filter(kw => 
    kw.toLowerCase().includes(primaryKeyword.toLowerCase())
  ).slice(0, 2));
  
  return [...new Set(baseKeywords)]; // Remove duplicates
};

